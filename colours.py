import operator

def colour_name(rgb):
  """
  Returns the name of the colour shown.
  This is done by comparing the colour
  with a list of colour names and finding the closest.
  """

  distances = {key: manhattan(rgb, val) for key, val in COLOUR_NAMES.iteritems()}
  return get_key_for_min(distances)

def manhattan(a, b):
  """Returns the Manhattan distance between two points"""
  return sum([abs(a_i - b_i) for a_i, b_i in zip(a, b)])

def get_key_for_min(d):
  """Returns the key for the minimum value in d"""
  return min(d.iteritems(), key=operator.itemgetter(1))[0]


